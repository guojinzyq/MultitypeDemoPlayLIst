package com.example.wjc.multitypedemoplaylist.data;

public class NewMusic {
    String title;
    int leftImage;
    String leftText;
    int centerImage;
    String centerText;
    int rightImage;
    String rightText;

    public String getTitle() {
        return title;
    }

    public int getLeftImage() {
        return leftImage;
    }

    public String getLeftText() {
        return leftText;
    }

    public int getCenterImage() {
        return centerImage;
    }

    public String getCenterText() {
        return centerText;
    }

    public int getRightImage() {
        return rightImage;
    }

    public String getRightText() {
        return rightText;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setLeftImage(int leftImage) {
        this.leftImage = leftImage;
    }

    public void setLeftText(String leftText) {
        this.leftText = leftText;
    }

    public void setCenterImage(int centerImage) {
        this.centerImage = centerImage;
    }

    public void setCenterText(String centerText) {
        this.centerText = centerText;
    }

    public void setRightImage(int rightImage) {
        this.rightImage = rightImage;
    }

    public void setRightText(String rightText) {
        this.rightText = rightText;
    }
}
